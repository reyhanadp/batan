    private void tampil_tabel() {
        Object[] baris = {"Nama", "NIP", "Bagian", "Bidang", "Golongan"};
        tabmode = new DefaultTableModel(null, baris);
        tabelJabatFungsionalNonPeneliti.setModel(tabmode);
        Connection con = DatabaseConnectivity.getConnection();
        try {
            String sql = "select nama,nip,bagian,bidang,gol from jab_fung";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String[] data = {hasil.getString(1), hasil.getString(2), hasil.getString(3), hasil.getString(4),
                     hasil.getString(5)};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }

